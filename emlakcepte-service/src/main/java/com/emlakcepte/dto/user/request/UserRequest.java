package com.emlakcepte.dto.user.request;

public class UserRequest {

	private String name;
	private String email;
	private String password;
	
	public UserRequest() {}

	public String getName() {
		return name;
	}

	public String getEmail() {
		return email;
	}

	public String getPassword() {
		return password;
	}
	
	
}
